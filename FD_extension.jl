begin
	function f(x)
		sin(x[1]^2) * 3x[2]^2
	end
	function h(x)
		sin(x[1]^2) * 3x[2]^2
	end
	# 
	function f′(x)
		[cos(x[1]^2)*2x[1] * 3x[2]^2, 6x[2]*sin(x[1]^2), 0]
	end
	# 
	function f(d::Vector{Dual{T,V,N}}) where {T,V,N}
		nt = ntuple(i->partials(d, i), N)
		# 
		x0 = value.(d)
		f0 = f(x0)
		f′0 = f′(x0)
		# 
		nt = sum(i->f′0[i]*partials.(d, i), 1:N)
		Dual{T}(f(x0), Tuple(nt))
	end
end

