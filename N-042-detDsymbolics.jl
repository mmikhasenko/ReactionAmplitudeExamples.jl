# ╔═╡ d2ba222c-7d35-4aa4-9f53-2b9d2a1346eb
md"""
# Scattering parameters for coupled-channel case
"""

# ╔═╡ 10669d26-af37-42da-9599-aa463acae554
@syms ρ[1:3] K[1:3, 1:3] ;

# ╔═╡ a95ac36e-4177-4741-a9b7-758043ddb646
one = Matrix(I,(3,3)) ;

# ╔═╡ 8acce6a4-4f40-4320-a70f-1f4a1c9ab640
md"""
Denominator of the scattering matrix reads:

$D = 1 - i\rho K.$

where $\rho$ is a diagonal matrix, $K$ is a arbitrary matrix of the same dimension.
"""

# ╔═╡ 46e19609-cf8a-45c2-9941-8a7b11e4334f
md"""
## Two-channels
"""

# ╔═╡ 07dbace5-e198-4ea7-ba2b-fc8933c9227a
D_2x2 = one[1:2,1:2] - 1im * Diagonal(ρ[1:2]) * K[1:2,1:2]

# ╔═╡ e3a10fc9-14de-4e5f-89bc-e6301dea4f81
detD_2x2 = det(D_2x2)

# ╔═╡ 0399a20b-9765-4aab-a69c-e36b53fbfc57
md"""
$\mathrm{det}D = b+a \rho_1$
"""

# ╔═╡ 990c1e32-0e73-4b4a-944f-21089b92eadb
NamedTuple{(:a,:b)}(sympy.poly(detD_2x2, ρ[1]).coeffs())

# ╔═╡ 31279f17-5a42-4d04-956d-3dc2376dc01d
md"""
## Three-channels
"""

# ╔═╡ 14d0be4f-9858-43ae-ba70-b3c06732194d
D_3x3 = one[1:3,1:3] - 1im * Diagonal(ρ[1:3]) * K[1:3,1:3]

# ╔═╡ bea87004-46b1-4e0d-9911-ddbd62a4b789
detD_3x3 = det(D_3x3) ;

# ╔═╡ d9956824-1439-48ae-8c61-8752d022733e
md"""
$\mathrm{det}D = b+a \rho_1$
"""

# ╔═╡ 33efdba8-98cd-4ac4-9eec-777892a8ada7
NamedTuple{(:a,:b)}(sympy.poly(detD_3x3, ρ[1]).coeffs())

