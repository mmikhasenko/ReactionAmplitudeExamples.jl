# ╔═╡ aad22ad4-17d8-4a2b-a4e7-1aaceb33124a
function integrand(y1,y2; pars)
	@unpack α1, α2, s1, s = pars
	y1^(-1-α1)/(1-y1*s1) * 
	y2^(-1-α2)/(1-y1*y2*s)
end

# ╔═╡ 38649702-1ec4-4a38-81cc-09b511e910a7
I(pars) = cuhre(2) do x,f
	f[1] = integrand(x[1],x[2]; pars)
	nothing
end.integral[1]

